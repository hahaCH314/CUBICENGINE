/* ═══════════════════════════════════════════
   Bedrock Addon Export
   Formats:
     mcaddon → BP + RP bundled in one zip
     mcpack  → BP-only .mcpack + RP-only .mcpack (two downloads)
     zip     → same as mcaddon but .zip extension
   ═══════════════════════════════════════════ */
/** data URL → Uint8Array（カスタムアイコン用） */
async function dataUrlToBytes(dataUrl: string): Promise<Uint8Array> {
  const res = await fetch(dataUrl);
  return new Uint8Array(await res.arrayBuffer());
}

export async function exportBedrock(state: EditorState, jsCode: string) {
  const name = sanitizeSlug(state.projectName);
  const desc = `${escComment(state.projectName)} — Generated by CUBICENGINE`;
  const compression = (state.compress ? "DEFLATE" : "STORE") as "DEFLATE" | "STORE";
  const compressionOptions = state.compress ? { level: 6 } : undefined;

  const bpUuid = uuid();
  const rpUuid = uuid();
  const bpModuleUuid = uuid();
  const rpModuleUuid = uuid();

  // アイコン: カスタム画像があればそれを使う、なければデフォルト
  const iconBytes = state.packIconDataUrl
    ? await dataUrlToBytes(state.packIconDataUrl)
    : createPlaceholderIcon();

  /* ── Build Behavior Pack folder contents ── */
  async function fillBP(bp: JSZip) {
    bp.file(
      "manifest.json",
      JSON.stringify(
        {
          format_version: 2,
          header: {
            name: `${state.projectName} BP`,
            description: desc,
            uuid: bpUuid,
            version: [1, 0, 0],
            // 1.20.10 以降ならどのバージョンでもロード可能
          min_engine_version: [1, 20, 10],
          },
          modules: [
            {
              type: "script",
              language: "javascript",
              uuid: bpModuleUuid,
              entry: "scripts/main.js",
              version: [1, 0, 0],
            },
          ],
          dependencies: [
            // ベータAPI ON=実験的スクリプトAPI(-beta / ワールドの「ベータAPI」実験が必要)
            // OFF=安定版(1.6.0 / Minecraft 1.20.30 以降・実験的機能不要)
            { module_name: "@minecraft/server", version: state.betaApi ? "1.6.0-beta" : "1.6.0" },
            // @minecraft/server-ui は使用時のみ追加（常時追加するとロード失敗の原因になる）
            ...(jsCode.includes("ActionFormData") || jsCode.includes("ModalFormData") || jsCode.includes("MessageFormData")
              ? [{ module_name: "@minecraft/server-ui", version: state.betaApi ? "1.1.0-beta" : "1.1.0" }]
              : []),
            { uuid: rpUuid, version: [1, 0, 0] },
          ],
          // capabilities は不要（script_eval 等の非公式フラグはロードを妨げる）
        },
        null,
        2,
      ),
    );

    // ★ jsCode はすでに import 文・console.warn ログを含むので二重追加しない
    const mainJsBody = jsCode.trim()
      ? jsCode
      : [
          `// ${escComment(state.projectName)} — CUBICENGINE`,
          `// Generated: ${new Date().toISOString()}`,
          ``,
          `import { world, system } from "@minecraft/server";`,
          ``,
          `console.warn("[${escJs(state.projectName)}] ロジックブロックが空です");`,
        ].join("\n");
    bp.file("scripts/main.js", NOTICE_COMMENT + "\n\n" + mainJsBody);

    // 悪用防止の注意喚起を同梱
    bp.file("NOTICE.txt", NOTICE_TEXT);

    bp.file("pack_icon.png", iconBytes);

    for (const block of state.blocks) {
      const components: Record<string, unknown> = {
        // unit_cube = 標準の1×1×1キューブ（カスタムジオメトリ不要）
        "minecraft:unit_cube": {},
        "minecraft:material_instances": {
          "*": { texture: `cubicengine_${block.name}`, render_method: "opaque" },
        },
      };
      // 本格モード(registered)のブロックは設定を反映
      if (block.registered) {
        if (typeof block.hardness === "number") {
          components["minecraft:destructible_by_mining"] = { seconds_to_destroy: Math.max(0, block.hardness) };
        }
        if (block.lightLevel && block.lightLevel > 0) {
          components["minecraft:light_emission"] = Math.min(15, Math.max(0, Math.round(block.lightLevel)));
        }
      }
      bp.file(
        `blocks/${block.name}.json`,
        JSON.stringify(
          {
            format_version: "1.20.10",
            "minecraft:block": {
              description: {
                identifier: `cubicengine:${block.name}`,
                menu_category: { category: "construction" },
              },
              components,
            },
          },
          null,
          2,
        ),
      );
    }
  }

  /* ── Build Resource Pack folder contents ── */
  async function fillRP(rp: JSZip) {
    rp.file(
      "manifest.json",
      JSON.stringify(
        {
          format_version: 2,
          header: {
            name: `${state.projectName} RP`,
            description: desc,
            uuid: rpUuid,
            version: [1, 0, 0],
            min_engine_version: [1, 20, 10],
          },
          modules: [{ type: "resources", uuid: rpModuleUuid, version: [1, 0, 0] }],
        },
        null,
        2,
      ),
    );

    rp.file("pack_icon.png", iconBytes);

    const textureData: Record<string, { textures: string }> = {};
    const blocksJson: Record<string, { textures: string; sound: string }> = {};
    for (const block of state.blocks) {
      textureData[`cubicengine_${block.name}`] = { textures: `textures/blocks/${block.name}` };
      blocksJson[`cubicengine:${block.name}`] = { textures: `cubicengine_${block.name}`, sound: "stone" };
    }

    rp.file(
      "textures/terrain_texture.json",
      JSON.stringify(
        {
          resource_pack_name: state.projectName,
          texture_name: "atlas.terrain",
          padding: 8,
          num_mip_levels: 4,
          texture_data: textureData,
        },
        null,
        2,
      ),
    );
    rp.file("blocks.json", JSON.stringify(blocksJson, null, 2));

    // 表示名(lang): 本格ブロックは displayName を、それ以外はブロック名をゲーム内名に
    const langLines = state.blocks
      .map((b) => `tile.cubicengine:${b.name}.name=${(b.registered && b.displayName) ? b.displayName : b.name}`)
      .join("\n");
    rp.file("texts/en_US.lang", langLines + "\n");
    rp.file("texts/languages.json", JSON.stringify(["en_US"], null, 2));

    for (const block of state.blocks) {
      const tex = await createColoredTexture(block.faces.top.color);
      rp.file(`textures/blocks/${block.name}.png`, tex);
    }

    // カスタムジオメトリは不要（unit_cube を使用するため）
  }

  const genOpts = { type: "blob" as const, compression, compressionOptions };

  if (state.exportFormat === "mcpack") {
    // Export Behavior Pack and Resource Pack as separate .mcpack files
    const bpZip = new JSZip();
    await fillBP(bpZip);
    const bpBlob = await bpZip.generateAsync(genOpts);
    downloadBlob(bpBlob, `${name}_BP.mcpack`);

    const rpZip = new JSZip();
    await fillRP(rpZip);
    const rpBlob = await rpZip.generateAsync(genOpts);
    downloadBlob(rpBlob, `${name}_RP.mcpack`);
  } else {
    // mcaddon or zip: bundle both packs together
    const zip = new JSZip();
    const bp = zip.folder(`${name}_BP`)!;
    const rp = zip.folder(`${name}_RP`)!;
    await fillBP(bp);
    await fillRP(rp);

    const ext = state.exportFormat === "zip" ? ".zip" : ".mcaddon";
    const blob = await zip.generateAsync(genOpts);
    downloadBlob(blob, `${name}${ext}`);
  }

  return true;
}

/* ═══════════════════════════════════════════
   Java Edition Export (Forge 1.20.1-47.3.0)
   ═══════════════════════════════════════════ */
export async function exportJava(state: EditorState, jsCode: string) {
  const zip = new JSZip();
  const name = sanitizeSlug(state.projectName);
  const className = sanitizeClassName(state.projectName);
  const modId = sanitizeModId(name);
  const pkg = `com.cubicengine.${modId}`;
  const pkgPath = `com/cubicengine/${modId}`;
  const compression = (state.compress ? "DEFLATE" : "STORE") as "DEFLATE" | "STORE";
  const compressionOptions = state.compress ? { level: 6 } : undefined;

  // ─── build.gradle ───
  zip.file(
    "build.gradle",
    [
      `plugins {`,
      `    id 'net.minecraftforge.gradle' version '6.0.+'`,
      `}`,
      ``,
      `version = '1.0.0'`,
      `group = '${pkg}'`,
      `archivesBaseName = '${modId}'`,
      ``,
      `java.toolchain.languageVersion = JavaLanguageVersion.of(17)`,
      ``,
      `minecraft {`,
      `    mappings channel: 'official', version: '1.20.1'`,
      `    runs {`,
      `        client { workingDirectory project.file('run') }`,
      `        server { workingDirectory project.file('run') }`,
      `    }`,
      `}`,
      ``,
      `dependencies {`,
      `    minecraft 'net.minecraftforge:forge:1.20.1-47.3.0'`,
      `}`,
    ].join("\n"),
  );

  // ─── gradle.properties ───
  zip.file(
    "gradle.properties",
    [
      `org.gradle.jvmargs=-Xmx3G`,
      `org.gradle.daemon=false`,
    ].join("\n"),
  );

  // ─── settings.gradle ───
  zip.file(
    "settings.gradle",
    [
      `pluginManagement {`,
      `    repositories {`,
      `        gradlePluginPortal()`,
      `        maven { url = 'https://maven.minecraftforge.net/' }`,
      `    }`,
      `}`,
      `rootProject.name = '${modId}'`,
    ].join("\n"),
  );

  // ─── Gradle Wrapper（本物・Forge 1.20.1 MDK 由来 / Gradle 8.8）───
  // これを同梱することで、ユーザーは JDK17 だけで `./gradlew build` 一発で通る。
  // ※P1.5/P1.6(2026-07-01)で final jar まで実ビルド検証済み。バイナリ(jar)や改行を壊さぬよう base64 で格納。
  // 99KB あるため、Java出力時のみ動的importで読み込む（Bedrock利用者のバンドルを太らせない）。
  const {
    GRADLEW_SH_B64,
    GRADLEW_BAT_B64,
    GRADLE_WRAPPER_JAR_B64,
    GRADLE_WRAPPER_PROPERTIES_B64,
  } = await import("../../lib/gradleWrapper");
  zip.file("gradle/wrapper/gradle-wrapper.properties", GRADLE_WRAPPER_PROPERTIES_B64, { base64: true });
  zip.file("gradle/wrapper/gradle-wrapper.jar", GRADLE_WRAPPER_JAR_B64, { base64: true });
  zip.file("gradlew", GRADLEW_SH_B64, { base64: true, unixPermissions: 0o755 });
  zip.file("gradlew.bat", GRADLEW_BAT_B64, { base64: true });

  // ─── Main Mod Class ───
  const src = zip.folder(`src/main/java/${pkgPath}`)!;

  src.file(
    `${className}Mod.java`,
    [
      `package ${pkg};`,
      ``,
      `import net.minecraftforge.common.MinecraftForge;`,
      `import net.minecraftforge.fml.common.Mod;`,
      `import net.minecraftforge.fml.event.lifecycle.FMLCommonSetupEvent;`,
      `import net.minecraftforge.fml.javafmlmod.FMLJavaModLoadingContext;`,
      `import org.apache.logging.log4j.LogManager;`,
      `import org.apache.logging.log4j.Logger;`,
      ``,
      `@Mod("${modId}")`,
      `public class ${className}Mod {`,
      `    public static final String MOD_ID = "${modId}";`,
      `    private static final Logger LOGGER = LogManager.getLogger();`,
      ``,
      `    public ${className}Mod() {`,
      `        var bus = FMLJavaModLoadingContext.get().getModEventBus();`,
      `        bus.addListener(this::setup);`,
      `        ModBlocks.register(bus);`,
      `        ModItems.register(bus);`,
      `        MinecraftForge.EVENT_BUS.register(this);`,
      `        LOGGER.info("[${escJava(state.projectName)}] Mod initialized!");`,
      `    }`,
      ``,
      `    private void setup(final FMLCommonSetupEvent event) {`,
      `        LOGGER.info("[${escJava(state.projectName)}] Setup complete!");`,
      `    }`,
      `}`,
    ].join("\n"),
  );

  // ─── Block Registration ───
  src.file(
    "ModBlocks.java",
    [
      `package ${pkg};`,
      ``,
      `import net.minecraft.world.level.block.Block;`,
      `import net.minecraft.world.level.block.state.BlockBehaviour;`,
      `import net.minecraftforge.registries.DeferredRegister;`,
      `import net.minecraftforge.registries.ForgeRegistries;`,
      `import net.minecraftforge.registries.RegistryObject;`,
      `import net.minecraftforge.eventbus.api.IEventBus;`,
      ``,
      `public class ModBlocks {`,
      `    public static final DeferredRegister<Block> BLOCKS =`,
      `        DeferredRegister.create(ForgeRegistries.BLOCKS, ${className}Mod.MOD_ID);`,
      ``,
      ...state.blocks.map((b) => {
        const bn = sanitizeBlockName(b.name);
        return [
          `    public static final RegistryObject<Block> ${bn.toUpperCase()} =`,
          `        BLOCKS.register("${escJava(bn)}",`,
          `            () -> new Block(BlockBehaviour.Properties.of()`,
          `                .strength(3.0f).requiresCorrectToolForDrops()));`,
        ].join("\n");
      }),
      ``,
      `    public static void register(IEventBus bus) {`,
      `        BLOCKS.register(bus);`,
      `    }`,
      `}`,
    ].join("\n"),
  );

  // ─── Item Registration ───
  src.file(
    "ModItems.java",
    [
      `package ${pkg};`,
      ``,
      `import net.minecraft.world.item.BlockItem;`,
      `import net.minecraft.world.item.Item;`,
      `import net.minecraftforge.registries.DeferredRegister;`,
      `import net.minecraftforge.registries.ForgeRegistries;`,
      `import net.minecraftforge.registries.RegistryObject;`,
      `import net.minecraftforge.eventbus.api.IEventBus;`,
      ``,
      `public class ModItems {`,
      `    public static final DeferredRegister<Item> ITEMS =`,
      `        DeferredRegister.create(ForgeRegistries.ITEMS, ${className}Mod.MOD_ID);`,
      ``,
      ...state.blocks.map((b) => {
        const bn = sanitizeBlockName(b.name);
        return [
          `    public static final RegistryObject<Item> ${bn.toUpperCase()}_ITEM =`,
          `        ITEMS.register("${escJava(bn)}",`,
          `            () -> new BlockItem(ModBlocks.${bn.toUpperCase()}.get(),`,
          `                new Item.Properties()));`,
        ].join("\n");
      }),
      ``,
      `    public static void register(IEventBus bus) {`,
      `        ITEMS.register(bus);`,
      `    }`,
      `}`,
    ].join("\n"),
  );

  // ─── Event Handler (積み木グラフ → 実 Java ロジック) ───
  // ★ 旧版は Bedrock用JSをコメント化するだけで何も動かなかった。
  //   logicGraphJson から CBlock[] を復元し、codegenJava で本物の
  //   @SubscribeEvent ハンドラを生成する。
  const logicBlocks = parseLogicBlocks(state.logicGraphJson);
  src.file(
    "ModEventHandler.java",
    NOTICE_COMMENT + "\n" + buildJavaModEventHandler(logicBlocks, {
      pkg,
      className,
      projectName: state.projectName,
    }),
  );

  // ─── mods.toml ───
  const resources = zip.folder("src/main/resources")!;
  const metaInf = resources.folder("META-INF")!;

  metaInf.file(
    "mods.toml",
    [
      `modLoader="javafml"`,
      `loaderVersion="[47,)"`,
      `license="MIT"`,
      ``,
      `[[mods]]`,
      `modId="${modId}"`,
      `version="1.0.0"`,
      `displayName="${escToml(state.projectName)}"`,
      `description='''${escComment(state.projectName)} — Generated by CUBICENGINE'''`,
      ``,
      `[[dependencies.${modId}]]`,
      `    modId="forge"`,
      `    mandatory=true`,
      `    versionRange="[47,)"`,
      `    ordering="NONE"`,
      `    side="BOTH"`,
      ``,
      `[[dependencies.${modId}]]`,
      `    modId="minecraft"`,
      `    mandatory=true`,
      `    versionRange="[1.20.1,1.21)"`,
      `    ordering="NONE"`,
      `    side="BOTH"`,
    ].join("\n"),
  );

  // pack.mcmeta
  resources.file(
    "pack.mcmeta",
    JSON.stringify({ pack: { description: `${state.projectName} Resources`, pack_format: 15 } }, null, 2),
  );

  // Block models, blockstates, item models, textures, lang
  const langEntries: Record<string, string> = {};
  for (const block of state.blocks) {
    resources.file(
      `assets/${modId}/blockstates/${block.name}.json`,
      JSON.stringify({ variants: { "": { model: `${modId}:block/${block.name}` } } }, null, 2),
    );
    resources.file(
      `assets/${modId}/models/block/${block.name}.json`,
      JSON.stringify({ parent: "block/cube_all", textures: { all: `${modId}:block/${block.name}` } }, null, 2),
    );
    resources.file(
      `assets/${modId}/models/item/${block.name}.json`,
      JSON.stringify({ parent: `${modId}:block/${block.name}` }, null, 2),
    );
    const tex = await createColoredTexture(block.faces.top.color);
    resources.file(`assets/${modId}/textures/block/${block.name}.png`, tex);

    langEntries[`block.${modId}.${block.name}`] = block.name
      .replace(/_/g, " ")
      .replace(/\b\w/g, (c) => c.toUpperCase());
  }
  resources.file(`assets/${modId}/lang/en_us.json`, JSON.stringify(langEntries, null, 2));

  // README
  zip.file(
    "README.md",
    [
      `# ${state.projectName}`,
      ``,
      `Generated by **CUBICENGINE** on ${new Date().toLocaleDateString("ja-JP")}`,
      ``,
      `## ⚠️ 利用上の注意 / Responsible Use`,
      ``,
      "```",
      NOTICE_TEXT,
      "```",
      ``,
      `## ビルド方法 / Setup`,
      `必要なのは **JDK 17** だけです（Gradleラッパーは同梱済み）。`,
      ``,
      `1. **JDK 17** をインストール（例: https://adoptium.net/ ）`,
      `2. このフォルダで次を実行（初回は Gradle と Minecraft/Forge を自動DL）:`,
      `   - Windows: \`gradlew.bat build\``,
      `   - Mac / Linux: \`./gradlew build\``,
      `3. できあがった \`build/libs/${modId}-1.0.0.jar\` を、`,
      `   Forge 1.20.1 を導入した Minecraft の \`mods/\` フォルダに入れる`,
      `4. Minecraft（Forge 1.20.1）を起動すれば Mod が読み込まれます`,
      ``,
      `## Blocks`,
      ...state.blocks.map((b) => `- \`${b.name}\``),
    ].join("\n"),
  );

  // 悪用防止の注意喚起を同梱
  zip.file("NOTICE.txt", NOTICE_TEXT);

  const blob = await zip.generateAsync({
    type: "blob",
    compression,
    compressionOptions,
  });
  downloadBlob(blob, `${name}-forge-mod.zip`);
  return true;
}

/* ═══════════════════════════════════════════
   Main Export Dispatcher
   ═══════════════════════════════════════════ */
export async function exportProject(state: EditorState, jsCode: string): Promise<boolean> {
  if (state.targetPlatform === "bedrock") {
    return exportBedrock(state, jsCode);
  } else {
    return exportJava(state, jsCode);
  }
}
