/** store.logicGraphJson から積み木グラフ(CBlock[])を復元（壊れていたら空配列） */
function parseLogicBlocks(json: string): CBlock[] {
  try {
    const data = JSON.parse(json || "{}");
    return Array.isArray(data?.blocks) ? (data.blocks as CBlock[]) : [];
  } catch {
    return [];
  }
}

/* ═══════════════════════════════════════════
   Security Sanitizers
   ═══════════════════════════════════════════ */

function escJs(s: string): string {
  return s
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/`/g, "\\`")
    .replace(/\$/g, "\\$")
    .replace(/\n/g, "\\n")
    .replace(/\r/g, "\\r")
    .replace(/\0/g, "\\0");
}

function escJava(s: string): string {
  return s
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\n/g, "\\n")
    .replace(/\r/g, "\\r")
    .replace(/\t/g, "\\t");
}

function escToml(s: string): string {
  return s
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\n/g, "\\n")
    .replace(/\r/g, "\\r");
}

function escComment(s: string): string {
  return s.replace(/[\n\r]/g, " ");
}

function sanitizeMcId(s: string): string {
  return s.replace(/[^a-z0-9_-]/g, "");
}

function sanitizeSlug(s: string): string {
  const slug = s.replace(/\s+/g, "_").replace(/[^a-z0-9_-]/gi, "").toLowerCase();
  return slug || "project";
}

function sanitizeClassName(s: string): string {
  const cls = s.replace(/\s+/g, "").replace(/[^a-zA-Z0-9]/g, "");
  const safe = /^\d/.test(cls) ? `Mod${cls}` : cls;
  return safe || "Mod";
}

function sanitizeModId(s: string): string {
  const id = s.replace(/[^a-z0-9_]/g, "");
  return id || "mod";
}

function sanitizeBlockName(s: string): string {
  return sanitizeMcId(s) || "block";
}

/* ═══════════════════════════════════════════
   UUID Generator
   ═══════════════════════════════════════════ */
function uuid(): string {
  return "xxxxxxxx-xxxx-4xxx-yxxx-xxxxxxxxxxxx".replace(/[xy]/g, (c) => {
    const r = (Math.random() * 16) | 0;
    const v = c === "x" ? r : (r & 0x3) | 0x8;
    return v.toString(16);
  });
}

/* ═══════════════════════════════════════════
   Download Helper
   ═══════════════════════════════════════════ */
function downloadBlob(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

/* ═══════════════════════════════════════════
   Colored Texture (16x16 PNG — per-pixel hue variation)
   ═══════════════════════════════════════════ */
function createColoredTexture(hexColor: string): Promise<Uint8Array> {
  return new Promise((resolve) => {
    const canvas = document.createElement("canvas");
    canvas.width = 16;
    canvas.height = 16;
    const ctx = canvas.getContext("2d")!;

    const r = parseInt(hexColor.slice(1, 3), 16) || 0;
    const g = parseInt(hexColor.slice(3, 5), 16) || 0;
    const b = parseInt(hexColor.slice(5, 7), 16) || 0;

    // Minecraft-style: each pixel shifts brightness slightly
    for (let x = 0; x < 16; x++) {
      for (let y = 0; y < 16; y++) {
        const d = (Math.random() - 0.5) * 40;
        const pr = Math.min(255, Math.max(0, Math.round(r + d)));
        const pg = Math.min(255, Math.max(0, Math.round(g + d)));
        const pb = Math.min(255, Math.max(0, Math.round(b + d)));
        ctx.fillStyle = `rgb(${pr},${pg},${pb})`;
        ctx.fillRect(x, y, 1, 1);
      }
    }

    canvas.toBlob(
      (blob) => blob!.arrayBuffer().then((buf) => resolve(new Uint8Array(buf))),
      "image/png",
    );
  });
}

